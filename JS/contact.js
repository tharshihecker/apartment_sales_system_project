function show(name){
if (name=="a1"){
document.getElementById("image1").src ="images/Instagram.webp";
  image1.style.display = "block";
}
else if (name=="a2"){
document.getElementById("image1").src ="images/facebook.jpg";
 image1.style.display = "block";
}
else if (name=="a3"){
document.getElementById("image1").src ="images/whatsapp.png";
 image1.style.display = "block";
}
}
